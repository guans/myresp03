package test;


//节点类
	public class Node {
		/**
		 * number 结点的序号
		 */
		int number;

		/**
		 * type 结点的序号
		 */
		int type;

		/**
		 * coor_x 结点x坐标
		 */
		double coor_x;

		/**
		 * coor_y 结点y坐标
		 */
		double coor_y;

		/**
		 * coor_z 结点z坐标
		 */
		double coor_z;

		/**
		 * weight 存储结点的权重
		 */
		double weight;

		/**
		 * next 结点的邻接结点
		 */
		Node next = null;

		public Node(double a, double b, double c) {

			coor_x = a;
			coor_y = b;
			coor_z = c;
		}

		Node(Node p) {
			coor_x = p.coor_x;
			coor_y = p.coor_y;
			coor_z = p.coor_z;
		}
	}
